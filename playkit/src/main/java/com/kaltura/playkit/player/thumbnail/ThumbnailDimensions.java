package com.kaltura.playkit.player.thumbnail;

public class ThumbnailDimensions {
    private final int width;
    private final int height;

    public ThumbnailDimensions(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
